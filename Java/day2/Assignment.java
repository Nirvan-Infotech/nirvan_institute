public class Assignment {
    /*
     * Assignment Operators 
     * a = a + b -> a+=b
     * a + b -> 1
     * a = a + b
     * 
     * a+=b
     * a-=b
     * 
     * a>>=b
     * a<<=b
     * a^=b
     * a|=b
     * a&=b
     */

     public static void main(String[] args) {
        int marks = 60;
        int inc = 5;

        marks += inc;
        System.out.println(marks);
     }
}
