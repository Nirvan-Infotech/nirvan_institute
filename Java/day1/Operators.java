public class Operators {
    public static void main(String[] args) {
        /*
        * Arithmatic Operators
        * Increment / Decrement
        * Assignment
        * Relational
        * Logical
        * Bitwise
         */

        //  Arithmatic Operators (+ - * / % ^)
        int a = 10, b = 20;
        /*
         * () {} []
         * ^
         * * /
         * + -
         */
        System.out.println("A+B = " + (a+b));
        System.out.println("A-B = " + (a-b));
        System.out.println("A*B = " + (a*b));
        System.out.println("A/B = " + (a/b)); // quotient -> double
        System.out.println("A%B = " + (a%b)); // reminder -> int
        System.out.println("A^B " + (a^b));
    }
}
